void solve() {
    ll n; in(n);
    vi arr_1(n), arr_2(n); in(arr_1); in(arr_2);

    bool is_possible = true;
    ll mx = 0;
    map<ll, int> mp;

    for(int i=0; i<n; i++) {
        if(arr_1[i] < arr_2[i]) {
            is_possible = false;
        } else {
            if(arr_2[i] == 0) {
                mx = max(mx, arr_1[i]);
            } else {
                ll diff = arr_1[i] - arr_2[i];
                mp[diff]++;
            }
        }
    }

    if(mp.size() > 1) {
        is_possible = false;
    }

    ll val = -1;
    for(auto [x, y]: mp) {
        val = x;
    }

    if(val != -1 && mx > val) {
        is_possible = false;
    }

    (is_possible)? out("YES"): out("NO");
}

