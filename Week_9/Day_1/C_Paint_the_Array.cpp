void solve() {
    ll n; in(n);
    vi arr(n); in(arr);

    ll gcd_1=0, gcd_2=0;

    for(int i=0; i<n; i++) {
        if(i%2 == 0) {
            gcd_1 = __gcd(gcd_1, arr[i]);
        } else {
            gcd_2 = __gcd(gcd_2, arr[i]);
        }
    }

    bool bl1=false, bl2=false;

    for(int i=0; i<n; i++) {
        if(i%2 != 0 && arr[i]%gcd_1 == 0) {
            bl1 = true;
        } else if(i%2 == 0 && arr[i]%gcd_2 == 0){
            bl2 = true;
        }
    }

    if(bl1 && bl2) {
        out(0);
    } else if(bl1) {
        out(gcd_2);
    } else {
        out(gcd_1);
    }
}

