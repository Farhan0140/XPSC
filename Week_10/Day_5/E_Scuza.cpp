void solve() {
    ll n, q; in(n, q);
    vi pre, mx_pre;
    pre.push_back(0);

    for(int i=0; i<n; i++) {
        ll x; in(x);
        pre.push_back(pre.back()+x);

        if(i == 0) {
            mx_pre.push_back(x);
        } else {
            mx_pre.push_back(max(mx_pre.back(), x));
        }
    }

    while(q--) {
        ll x; in(x);

        ll it = upper_bound(all(mx_pre), x) - mx_pre.begin();
        out(pre[it]);
    }
    out();
}

